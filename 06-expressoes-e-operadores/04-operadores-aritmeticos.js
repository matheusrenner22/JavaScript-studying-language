// Operadores aritméticos

// multiplicação
console.log(3 * 5);

// divisão
console.log(5 / 5);

// soma
console.log(5 + 5);

// subtração
console.log(5 - 5);

// resto da divisão
console.log(5 % 5);

// incremento
console.log(5++);
console.log(++5);

// decremento
console.log(5--);
console.log(--5);

// exponencial
console.log(5 ** 5);